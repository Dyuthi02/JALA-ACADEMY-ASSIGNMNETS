//2. Write a java program to print 1 to 20 numbers using the while loop.

package JALA_ACADEMY_ASSIGNMNETS.Loops;

public class WhileLoop 
{
    public static void main(String[] args) {
        int n=1;
        while(n<=20)
        {
            System.out.println(n);
            n++;
        }
    }    
}
