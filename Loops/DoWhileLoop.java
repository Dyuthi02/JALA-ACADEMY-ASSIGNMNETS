//7. Write a program to print 1 to 10 using the do-while loop statement.

package JALA_ACADEMY_ASSIGNMNETS.Loops;

public class DoWhileLoop {
    public static void main(String[] args) {
        int a=1;
        do{
            System.out.print(a+" ");
            a++;
        }
        while(a<=10);
    }
}
