//6. Write a program to print even number between 10 and 100 using while

package JALA_ACADEMY_ASSIGNMNETS.Loops;

public class Even 
{
    public static void main(String[] args) {
        int a=10;
        while(a<=100)
        {
            System.out.print(a+" ");
            a+=2;
        }
    }
}
